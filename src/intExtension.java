public class intExtension {
    public static void main(String[] args) {
            int i=10;
            int i1=0b10;
            int i2=010;
            int i3=0x10;
            System.out.println(i);
            System.out.println(i1);
            System.out.println(i2);
            System.out.println(i3);
        }
    }

